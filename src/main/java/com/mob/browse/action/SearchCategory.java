package com.mob.browse.action;

import java.util.ArrayList;
import java.util.List;

public class SearchCategory {

	private String categoryHeading;
	private List<CategoryItem> items = new ArrayList<CategoryItem>();
	
	public String getCategoryHeading() {
		return categoryHeading;
	}
	public void setCategoryHeading(String categoryHeading) {
		this.categoryHeading = categoryHeading;
	}
	public List<CategoryItem> getItems() {
		return items;
	}
	public void setItems(List<CategoryItem> items) {
		this.items = items;
	}
	
	public void addCategoryItem(CategoryItem item){
		items.add(item);
	}
}
